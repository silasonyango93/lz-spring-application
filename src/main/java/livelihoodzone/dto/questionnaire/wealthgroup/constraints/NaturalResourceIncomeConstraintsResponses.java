package livelihoodzone.dto.questionnaire.wealthgroup.constraints;

public class NaturalResourceIncomeConstraintsResponses {
    private int decliningNaturalResources;
    private int populationPressure;
    private int naturalresourceExploitationRights;
    private int lowValueNrBasedProducts;

    public int getDecliningNaturalResources() {
        return decliningNaturalResources;
    }

    public void setDecliningNaturalResources(int decliningNaturalResources) {
        this.decliningNaturalResources = decliningNaturalResources;
    }

    public int getPopulationPressure() {
        return populationPressure;
    }

    public void setPopulationPressure(int populationPressure) {
        this.populationPressure = populationPressure;
    }

    public int getNaturalresourceExploitationRights() {
        return naturalresourceExploitationRights;
    }

    public void setNaturalresourceExploitationRights(int naturalresourceExploitationRights) {
        this.naturalresourceExploitationRights = naturalresourceExploitationRights;
    }

    public int getLowValueNrBasedProducts() {
        return lowValueNrBasedProducts;
    }

    public void setLowValueNrBasedProducts(int lowValueNrBasedProducts) {
        this.lowValueNrBasedProducts = lowValueNrBasedProducts;
    }
}
